                <div class="campaign-text">
                    <p>At Musgrave we are passionate about local produce and family-run businesses. Food Academy is a
                        programme that champions both, giving independent local producers a chance to bring their
                        product to the mass market. Through our 11 food and drink brands, we support thousands of
                        suppliers from across the island of Ireland, but Food Academy is unique in helping smaller,
                        start-up producers get their big break.</p>
                    <p>Now in its eighth year, Food Academy is a food business development programme, a partnership
                        between SuperValu, Bord Bia and the Local Enterprise Offices (LEOs). Participants receive
                        training in food safety, market research and branding, marketing, finance, sustainability and
                        business development. The programme currently supports 290 Irish food and drink producers, often
                        family-run businesses, making their products available 52 weeks of the year in their local
                        SuperValu stores.</p>
                    <p>The value for our business is in providing unique, artisan and local produce to shoppers who
                        value produce with provenance. Great quality and taste is synonymous with Irish food and drink
                        and the Food Academy programme shines a spotlight on the great food businesses around the
                        county.</p>
