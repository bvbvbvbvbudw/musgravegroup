                    <h2>Sustainability</h2>
                    <p>The Musgrave Sustainability Fund was launched in June 2022, providing €25 million to
                        support retailers to make reductions in their carbon emissions. By December 2022
                        Musgrave had paid out more than 42% of the Sustainability Fund, €8,964,916. This has
                        resulted in SuperValu and Centra stores reducing their carbon emissions by an estimated
                        3,809 tonnes of CO2 representing 42% of the total carbon reduction target for the
                        Sustainability Fund.</p>
