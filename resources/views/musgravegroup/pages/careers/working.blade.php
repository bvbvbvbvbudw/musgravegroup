                            <h2>Work Smart</h2>
                            <p>We have redesigned our ways of working to further empower our colleagues to focus on the
                                outcomes and contribution that everyone makes in their roles.</p>
                            <p>Work Smart @ Musgrave is our hybrid work model which allows colleagues the option to
                                alternate their time between connecting and collaborating in the office and working
                                remotely. It brings an opportunity to retain the best of the remote working experience
                                whilst enabling colleagues to connect in person and work together onsite. It supports
                                the delivery of our business growth strategy by enabling colleagues to work in a more
                                productive and agile way, focused on balancing work and life while delivering the very
                                best for our customers.</p>
                            <p>Across Musgrave, our roles allow for different levels of flexibility. Our operations and
                                retail roles work from a site location and other roles adopt a hybrid approach.</p>


